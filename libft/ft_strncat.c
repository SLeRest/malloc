#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char		*cs1;
	const char	*cs2;

	cs1 = s1;
	cs2 = s2;
	while (*cs1)
		cs1++;
	while (*cs2 && n--)
	{
		*cs1 = *cs2;
		cs1++;
		cs2++;
	}
	*cs1 = '\0';
	return (s1);
}
